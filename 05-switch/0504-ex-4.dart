void main() {
  int a = 2;
  switch (a) {
    case 0:
      print("SUNDAY");
      break;

    case 1:
      print("MONDAY");
      break;

    case 2:
      print("TUESDAY");
      break;

    case 3:
      print("WEDNESDAY");
      break;

    case 4:
      print("THURSDAY");
      break;

    case 5:
      print("FRIDAY");
      break;

    case 6:
      print("SATDAY");
      break;

    default:
      print("invalid value");
      break;
  }
}
